package unterkapitel_02_07;

public class Kinderlied {
	public static void main (String args[]) {
	
		sum();
		ersteStrophe();
		sum();
		System.out.println();
		sum();
		zweiteStrophe();
		sum();
	
	}
	public static void sum() {
		System.out.println("Summ, Summ, Summ, Bienchen, summ herum.");
	}
	
	public static void ersteStrophe() {

		System.out.println("Ei, wir tun dir nichts zuleide, flieg nur über Wald und Heide!");
	}
	
	public static void zweiteStrophe() {
		
		System.out.println("Such in Blumen, such in Blümchen dir ein Tröpfchen, dir ein Krümchen!");
		
	}
	
}
