package unterkapitel_03_09;

public class Pair {
	
	private Object one;
	private Object theOther;
	
	public Pair(Object one, Object theOther) {
		this.one = one;
		this.theOther = theOther;
	}
		
	
	public Object getOne() {
		return one;
	}
	
	public Object getTheother() {
		return theOther;
	}
		
}
